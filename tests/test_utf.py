@pytest.mark.parametrize('lib_fn, encoding_name', [
    (utf.encode_utf8, 'utf-8'),
    (utf.encode_utf16be, 'utf-16be'),
    (utf.encode_utf16le, 'utf-16le'),
    (utf.encode_utf32be, 'utf-32be'),
    (utf.encode_utf32le, 'utf-32le'),
])
def test_encoding(lib_fn, encoding_name):
    assert lib_fn(test_string) == test_string.encode(encoding_name)

@pytest.mark.parametrize('lib_fn, encoding_name', [
    (utf.decode_utf8, 'utf-8'),
    (utf.decode_utf16be, 'utf-16be'),
    (utf.decode_utf16le, 'utf-16le'),
    (utf.decode_utf32be, 'utf-32be'),
    (utf.decode_utf32le, 'utf-32le'),
])
def test_decoding(lib_fn, encoding_name):
    # lib_fn, encoding_name = 
    test_bytes = test_string.encode(encoding_name)
    assert lib_fn(test_bytes) == test_bytes.decode(encoding_name)
